package chapter3.arrays;

public class ArraysOfPrimitives {
    public static void main(String[] args){
        //ways to instantiate arrays:
        int[] numbers1 = new int[3]; //creates new array of size 3
        int[] numbers2 = new int[] {1, 2, 3}; //a bit redundant
        int[] numbers3 = {1, 2, 3}; //same as numbers 2 except less redundant

        //other formats:
        int [] numbers4;
        int numbers5[];
        int numbers6 [];

        int[] numbers7, numbers8;
        int numbers9[], numbers10; //numbers10 is just an int not an array

    }
}
