package chapter5.abstractClasses;

public class AbstractClassRules {
    /*
    Class rules:
    1. cannot be instantiated directly
    2. defined with any number of abstract/nonabstract methods including 0
    3. cannot be final or private
    4. extended abstract classes from abstract classes inherit all abstract methods as its own
    5. The first concrete class that extends an abstract class must have all methods implemented
     */
}
